// double
function doubleIt (number) {
    const doubled = number * 2;
    console.log(number, doubled);
}
doubleIt(15);
doubleIt(88);
const number = 55;
doubleIt(number);

// difference
function difference (num1, num2) {
    const diff = num1 - num2;
    console.log(num1, num2, "difference is", diff);
}
const motherAge = 50;
const myAge = 26;
difference(motherAge, myAge);