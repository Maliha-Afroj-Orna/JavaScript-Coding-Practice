// min
const min = Math.min(45, 97, 1, 65, 34);
console.log("Min: ", min);

// max
const max = Math.max(45, 97, 1, 65, 34);
console.log("Max: ", max);

// Pi
console.log(Math.PI);

// abs
console.log(Math.abs(-88));
console.log(Math.abs(10-5));
console.log(Math.abs(5-10));

// round
console.log(Math.round(4.25));

// floor
console.log(Math.floor(2.95));

// ceil
console.log(Math.ceil(6.2));

// random --> 0 to 1 --> fraction
console.log(Math.random());
// random --> 0 to 10 --> fraction
console.log(Math.random() * 10);
// round --> 0 to 10 --> Purnosonkha or Rounded number (no fraction)
const rand = Math.round(Math.random() * 10);
console.log(rand);
